    private void equalsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_equalsMouseClicked
        String res = make_operation( n, Integer.parseInt(label.getText()), operation );
        
        try{
            String[] parts = res.split("[.]");

            if( parts[1].equals("0") ){ 
                label.setText( parts[0] );
            }else{ 
                label.setText( res ); 
            }
            
        }catch( Exception e ){
            label.setText( "Error" ); 
        }
        operation = "CLEAR";
        n = 0;
    }//GEN-LAST:event_equalsMouseClicked


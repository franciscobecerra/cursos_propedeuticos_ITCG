    private String make_operation( int n1, int n2, String operation ){
        double res = 0;
        
//        System.out.println( n1 );
//        System.out.println( operation );
//        System.out.println( n2 );
        
        try{
            switch(operation){
                case "sum": {
                    res = (double) n1 + n2;
                    break;
                }
                case "sub": {
                    res = (double) n1 - n2;
                    break;
                }
                case "mul": {
                    res = (double) n1 * n2;
                    break;
                }
                case "div": {
                    res = (double) n1 / n2;
                    break;
                } 
                default : { res = 0; }
            }
            return Double.toString(res);
        }catch(Exception e){
            operation = "CLEAR";
            return "Error";
        }
    }
    
